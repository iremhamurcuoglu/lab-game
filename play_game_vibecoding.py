def in_bounds(x, y, w, h):
    return 0 <= x < w and 0 <= y < h

def neighbors_cells(x, y):
    return [(x, y-2), (x, y+2), (x-2, y), (x+2, y)]

def carve_passage(maze: List[List[str]], start: Tuple[int,int]):
    stack = [start]
    w, h = len(maze[0]), len(maze)
    while stack:
        x, y = stack[-1]
        maze[y][x] = FLOOR
        nbrs = neighbors_cells(x, y)
        random.shuffle(nbrs)
        moved = False
        for nx, ny in nbrs:
            if in_bounds(nx, ny, w, h) and maze[ny][nx] == WALL:
                wx, wy = (x + nx)//2, (y + ny)//2
                maze[wy][wx] = FLOOR
                maze[ny][nx] = FLOOR
                stack.append((nx, ny))
                moved = True
                break
        if not moved:
            stack.pop()

def make_maze(w: int, h: int) -> List[List[str]]:
    if w % 2 == 0: w -= 1
    if h % 2 == 0: h -= 1
    m = [[WALL for _ in range(w)] for _ in range(h)]
    carve_passage(m, (1, 1))
    return m

def empty_cells(maze):
    cells = []
    for y, row in enumerate(maze):
        for x, ch in enumerate(row):
            if ch == FLOOR:
                cells.append((x, y))
    return cells

def place_items(maze, symbol, count, forbid=set()):
    cells = [c for c in empty_cells(maze) if c not in forbid]
    random.shuffle(cells)
    placed = []
    for i in range(min(count, len(cells))):
        x, y = cells[i]
        maze[y][x] = symbol
        placed.append((x, y))
    return placed

def count_gold(maze):
    return sum(row.count(GOLD) for row in maze)

def move_if_possible(maze, px, py, dx, dy):
    nx, ny = px + dx, py + dy
    w, h = len(maze[0]), len(maze)
    if not in_bounds(nx, ny, w, h):            # sÄ±nÄ±r
        return px, py, 0
    if maze[ny][nx] == WALL:                   # duvar
        return px, py, 0
    gain = 0
    if maze[ny][nx] == GOLD:                   # altÄ±n
        gain = 10
        maze[ny][nx] = FLOOR
    return nx, ny, gain

def render_map(maze, px, py) -> str:
    rows = []
    for y, row in enumerate(maze):
        line = []
        for x, ch in enumerate(row):
            if (x, y) == (px, py):
                line.append(PLAYER)
            else:
                line.append(ch)
        rows.append("".join(line))
    return "\n".join(rows)

def init_game(seed=None):
    if seed is not None:
        random.seed(seed)
    maze = make_maze(MAZE_W, MAZE_H)
    start = random.choice(empty_cells(maze))
    px, py = start
    place_items(maze, GOLD, GOLD_COUNT, forbid={start})
    state = {
        "maze": maze,
        "px": px, "py": py,
        "score": 0,
        "steps_left": STEP_LIMIT,
        "game_over": False,
        "win": False,
    }
    return state

